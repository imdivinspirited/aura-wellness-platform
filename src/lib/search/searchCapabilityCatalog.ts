const ALL: CapabilityEntry[] = TITLES.map((title, index) => {
  const id = index + 1;
  return {
    id,
    bucket: bucketForId(id),
    slug: slugify(id, title),
    title,
    tier: tierForId(id),
  };
});

export const SEARCH_CHECKLIST_COUNT = 501 as const;

export const ALL_SEARCH_CAPABILITIES: readonly CapabilityEntry[] = ALL;

export function getSearchCapability(id: number): CapabilityEntry | undefined {
  if (id < 1 || id > 501) return undefined;
  return ALL[id - 1];
}

export function summarizeTiers(): { in_app: number; partial_client: number; requires_infrastructure: number } {
  let in_app = 0;
  let partial_client = 0;
  let requires_infrastructure = 0;
  for (const c of ALL) {
    if (c.tier === 'in_app') in_app += 1;
    else if (c.tier === 'partial_client') partial_client += 1;
    else requires_infrastructure += 1;
  }
  return { in_app, partial_client, requires_infrastructure };
}
