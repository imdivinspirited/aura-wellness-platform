const SLUG_SAFE = /[^a-z0-9]+/g;

function slugify(id: number, title: string): string {
  const base = title
    .toLowerCase()
    .replace(SLUG_SAFE, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 48);
  return `${id}-${base || 'item'}`;
}

