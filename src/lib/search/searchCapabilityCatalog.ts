export type ImplementationTier = 'in_app' | 'partial_client' | 'requires_infrastructure';

export type CapabilityEntry = {
  id: number;
  bucket: CapabilityBucket;
  /** Stable slug for i18n / analytics */
  slug: string;
  /** Display line — matches industry checklist ordering within bucket */
  title: string;
  tier: ImplementationTier;
};

const BUCKET_RANGES: { from: number; to: number; bucket: CapabilityBucket }[] = [
  { from: 1, to: 50, bucket: 'AI & ML' },
  { from: 51, to: 100, bucket: 'Core Search' },
  { from: 101, to: 150, bucket: 'UX & Interface' },
  { from: 151, to: 200, bucket: 'Performance' },
  { from: 201, to: 250, bucket: 'Security & Privacy' },
  { from: 251, to: 300, bucket: 'Personalization' },
  { from: 301, to: 350, bucket: 'Advanced Filtering' },
  { from: 351, to: 401, bucket: 'Analytics & Insights' },
  { from: 402, to: 451, bucket: 'Multi-Modal Search' },
  { from: 452, to: 501, bucket: 'Developer & API' },
];

function bucketForId(id: number): CapabilityBucket {
  for (const r of BUCKET_RANGES) {
    if (id >= r.from && id <= r.to) return r.bucket;
  }
  return 'Developer & API';
}

