export type CapabilityBucket =
  | 'AI & ML'
  | 'Core Search'
  | 'UX & Interface'
  | 'Performance'
  | 'Security & Privacy'
  | 'Personalization'
  | 'Advanced Filtering'
  | 'Analytics & Insights'
  | 'Multi-Modal Search'
