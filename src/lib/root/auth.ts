/**
 * Logout root user
 */
export function logoutRootUser(): void {
  const session = getSession();
  if (session) {
    // Log logout (in production, this would be an API call)
    logAuditAction({
      rootUserId: session.userId,
      action: 'logout',
      success: true,
    }).catch(console.error);
  }
  clearSession();
}

